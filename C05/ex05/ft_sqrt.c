int	ft_sqrt(int nb)
{
	long	nbr;
	long	i;

	nbr = nb;
	if (nbr <= 0)
	{
		return (0);
	}
	if (nbr == 1)
	{
		return (1);
	}
	i = 2;
	if (nbr >= 2)
	{
		while (i * i <= nbr)
		{
			if (i * i == nbr)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
